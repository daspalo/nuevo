x = float(input("Enter a number:"))       #takes an input from user and converts to a float
y = float(input("Enter another number:")) #takes an input from user and converts to a float
print("x + y:", x + y)                      #prints x + y
print("x - y:", x - y)                      #prints x - y
print("x * y:", x * y)                      #prints x * y
print("x / y:", x / y)                      #prints x / y
print("x % y:", x % y)                      #prints x and divides it by y and finds the remainder
print("x ** y:", x ** y)                    #prints x raised to the y power
print("x // y:", x // y)                    #prints x / y and takes out the decimal left over
