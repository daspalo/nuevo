studentavg = float(input("What is you average? "))

if studentavg >=90:
    print(" you have an A")
elif studentavg >=80:
    print(" you have a B")
elif studentavg >=70:
    print(" you have a C")
elif studentavg >=60:
    print(" you have a D")
elif studentavg >=0:
    print(" you have an F")

else:
    print(" you have typed in a negative number")
